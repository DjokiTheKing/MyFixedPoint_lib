inline fixed fixed::logn(const fixed &num)
{  
    int32_t shz = 17 - __builtin_clz(num.value);
    int32_t t0;
    if(shz >= 0){
        if (shz > 0 && (shz & (shz - 1)) != 0) {
            shz |= shz >> 1;
            shz |= shz >> 2;
            shz |= shz >> 4;
            shz |= shz >> 8;
            shz |= shz >> 16;
            shz++;
        }
        int16_t t = (num.value >> (shz + 6));
        t0 = -(int32_t(fixed::log_base_lookup[t]) << 2);
        int32_t interp = (shz >= 9)
            ? (num.value >> (shz - 9)) & 0x7FFF
            : (num.value << (9 - shz)) & 0x7FFF;
        int32_t delta = (-(int32_t(fixed::log_base_lookup[t + 1]) << 2)) - t0;
        t0 += (delta * interp) >> 15;
        t0 += (int32_t(log_pow2_lookup[shz]) << 4);
    }else{
        shz = -shz;
        if (shz > 0 && (shz & (shz - 1)) != 0) {
            shz |= shz >> 1;
            shz |= shz >> 2;
            shz |= shz >> 4;
            shz |= shz >> 8;
            shz |= shz >> 16;
            shz++;
            shz>>=1;
        }
        int16_t t = (num.value << shz) >> 6;
        t0 = -(int32_t(fixed::log_base_lookup[t]) << 2);
        int32_t interp = (num.value << (shz + 9)) & 0x7FFF;
        int32_t delta = (-(int32_t(fixed::log_base_lookup[t + 1]) << 2)) - t0;
        t0 += (delta * interp) >> 15;
        t0 -= (int32_t(log_pow2_lookup[shz]) << 4);
    }

    fixed res; 
    res.value = t0; 
    return res;
}

