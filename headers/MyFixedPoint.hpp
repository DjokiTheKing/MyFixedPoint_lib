inline  fixed __not_in_flash_func(fixed::sqrt_acc)(const fixed &  num)
{
    if(num.value <= 0) return 0;
    int32_t x0 = sqrt(num).value;
    int32_t div = int32_t((int64_t(num.value) << FRAC_BITS) / x0);
    x0  = (x0 + div + 1) >> 1;
    fixed result;
    result.value = x0;
    return result;
}
